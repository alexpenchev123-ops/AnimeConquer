function initGame() {
    // 1. Get Setup Data
    selectedVerse = document.getElementById('verse-select').value;
    const count = parseInt(document.getElementById('player-count').value);
    
    // 2. Initialize Pools
    const verseData = animeDB[selectedVerse];
    game.pools = {};
    for (let tier in verseData) {
        game.pools[tier] = [...verseData[tier]]; // Clone the data
    }

    // 3. Setup Players
    const colors = ['red', 'blue', 'green', 'yellow'];
    const corners = [0, 35, 5, 30];
    game.players = [];

    for (let i = 0; i < count; i++) {
        const nameInput = document.getElementById(`p-name-${i}`);
        game.players.push({
            name: nameInput ? nameInput.value || `Player ${i+1}` : `Player ${i+1}`,
            color: colors[i],
            hand: [],
            baseIdx: corners[i],
            eliminated: false
        });

        // Give Starting Cards
        for (let j = 0; j < 3; j++) {
            let roll = Math.random();
            let tier;
            if (selectedVerse === 'DemonSlayer') {
                tier = roll < 0.6 ? 'epic' : 'rare';
            } else if (selectedVerse === 'JJK') {
                // JJK common pool is small — draw from rare/epic to preserve it for the board
                tier = roll < 0.5 ? 'epic' : 'rare';
            } else if (selectedVerse === 'MHA') {
                tier = roll < 0.15 ? 'epic' : (roll < 0.6 ? 'rare' : 'common');
            } else {
                tier = roll < 0.15 ? 'epic' : (roll < 0.6 ? 'rare' : 'common');
            }
            const unit = pull(tier);
            if(unit) game.players[i].hand.push(unit);
        }
    }

    // 4. Generate Board
    const dsLegendarySpots = [];
    if (selectedVerse === 'DemonSlayer') {
        const center = [14, 15, 20, 21].sort(() => Math.random() - 0.5);
        dsLegendarySpots.push(center[0], center[1]);
    }

    for (let i = 0; i < 36; i++) {
        const pIdx = corners.indexOf(i);
        if (pIdx !== -1 && pIdx < count) {
            // Player Base
            game.grid[i] = { unit: null, owner: colors[pIdx] };
        } else {
            // Neutral Tiles
            const r = Math.floor(i / 6), c = i % 6;
            const dist = Math.min(r, 5 - r, c, 5 - c);
            let tier;

            if (selectedVerse === 'DemonSlayer') {
                if (dsLegendarySpots.includes(i)) tier = 'legendary';
                else if (dist >= 1) tier = (game.pools.mythic?.length > 0) ? 'mythic' : 'epic';
                else tier = 'common';
            } else {
                if (dist === 2) tier = 'legendary';
                else if (dist === 1) tier = (game.pools.mythic?.length > 0) ? 'mythic' : 'epic';
                else tier = 'common';
            }
            game.grid[i] = { unit: pull(tier), owner: 'neutral' };
        }
    }

    // 5. Start Game
    game.currentTurn = 0;
    game.actionUsed = false;
    
    // Sync if online
    if (isHost && conn) {
        conn.send({ type: 'START', state: game });
    }

    // Switch Screens
    document.getElementById('setup').classList.remove('active');
    document.getElementById('game-screen').classList.add('active');
    
    render();
}

function pull(tier) {
    const order = ['ultra', 'legendary', 'mythic', 'epic', 'rare', 'common'];
    let pool = game.pools[tier];

    if (pool && pool.length > 0) {
        const avail = pool.filter(u => !isCharacterInPlay(u.name));
        if (avail.length > 0) {
            const unitData = avail[Math.floor(Math.random() * avail.length)];
            game.pools[tier] = game.pools[tier].filter(u => u.name !== unitData.name);
            // Spread to ensure nextForm, cost, and tier are all copied
            return { ...unitData, tier: tier };
        }
    }

    // Fallback: try tiers below (toward common) first, then above (toward ultra) if still empty
    const startIdx = order.indexOf(tier);
    for (let i = startIdx + 1; i < order.length; i++) {
        const fallback = pull(order[i]);
        if (fallback) return fallback;
    }
    for (let i = startIdx - 1; i >= 0; i--) {
        const fallback = pull(order[i]);
        if (fallback) return fallback;
    }
    return null;
}

